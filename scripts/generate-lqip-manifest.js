#!/usr/bin/env node
// Generate a manifest of ultra-tiny base64 LQIPs keyed by image URL.
// The web runtime can then paint a fully-decoded blurred preview in the very
// first frame with zero network requests — the LQIPs are inlined into JS.
//
// Run: node scripts/generate-lqip-manifest.js
//      Writes js/lqip-manifest.js, re-run whenever data.js or articles change.

const fs = require('fs');
const path = require('path');
const https = require('https');

const ROOT = path.resolve(__dirname, '..');
const DATA_JS = path.join(ROOT, 'js', 'data.js');
const OUTPUT = path.join(ROOT, 'js', 'lqip-manifest.js');
const LQIP_WIDTH = 24;
const LQIP_QUALITY = 30;

// Extract image URLs from data.js without executing it.
function collectImageUrls() {
    const source = fs.readFileSync(DATA_JS, 'utf8');
    const urls = new Set();
    const regex = /image\s*:\s*["']([^"']+)["']/g;
    let match;
    while ((match = regex.exec(source)) !== null) {
        urls.add(match[1]);
    }
    return Array.from(urls);
}

function buildLqipUrl(src) {
    try {
        const url = new URL(src);
        if (!url.hostname.includes('static.wixstatic.com')) return null;
        const parts = url.pathname.split('/').filter(Boolean);
        const filename = parts[parts.length - 1];
        const hasTransform = parts.includes('v1');
        const height = Math.round(LQIP_WIDTH * 0.66);
        if (hasTransform) {
            return src
                .replace(/q_(\d{1,3})/g, `q_${LQIP_QUALITY}`)
                .replace(/w_(\d{2,4})/g, `w_${LQIP_WIDTH}`);
        }
        return `${src}/v1/fill/w_${LQIP_WIDTH},h_${height},al_c,q_${LQIP_QUALITY},enc_auto/${filename}`;
    } catch {
        return null;
    }
}

function fetchBinary(url) {
    return new Promise((resolve, reject) => {
        https.get(url, res => {
            if (res.statusCode !== 200) {
                res.resume();
                return reject(new Error(`HTTP ${res.statusCode} for ${url}`));
            }
            const chunks = [];
            res.on('data', c => chunks.push(c));
            res.on('end', () => resolve({
                buffer: Buffer.concat(chunks),
                type: res.headers['content-type'] || 'image/jpeg'
            }));
            res.on('error', reject);
        }).on('error', reject);
    });
}

async function main() {
    const urls = collectImageUrls();
    console.log(`[lqip] Found ${urls.length} source images in data.js`);

    const manifest = {};
    let ok = 0, skipped = 0, failed = 0;

    for (const src of urls) {
        const lqipUrl = buildLqipUrl(src);
        if (!lqipUrl) { skipped++; continue; }
        try {
            const { buffer, type } = await fetchBinary(lqipUrl);
            // Cap LQIP byte size at 4 KB — anything larger defeats the point.
            if (buffer.length > 4096) {
                console.warn(`[lqip] ${src} → ${buffer.length}B (skipped, too large)`);
                skipped++;
                continue;
            }
            manifest[src] = `data:${type};base64,${buffer.toString('base64')}`;
            ok++;
            process.stdout.write('.');
        } catch (err) {
            failed++;
            console.warn(`\n[lqip] failed for ${src}: ${err.message}`);
        }
    }

    const header = `// AUTO-GENERATED by scripts/generate-lqip-manifest.js — do not edit by hand.
// Maps article image URLs to ~1-3 KB base64 JPEG/WebP data URIs that paint
// instantly with zero network round-trips. Regenerate whenever data.js changes.
`;
    const body = `window.__LQIP_MANIFEST = ${JSON.stringify(manifest, null, 2)};\n`;
    fs.writeFileSync(OUTPUT, header + body);

    console.log(`\n[lqip] Wrote ${OUTPUT}`);
    console.log(`[lqip] ok=${ok} skipped=${skipped} failed=${failed}`);
}

main().catch(err => {
    console.error(err);
    process.exit(1);
});
